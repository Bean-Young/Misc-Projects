b=[1,0,-1];a=[1,0,-0.81];
x=ones(1,100);
y=filter(b,a,x);
plot(y,'k-x');
xlabel('n');ylabel('y(n)');hold on
n=0:99;v=-0.0556*(-0.9).^n+1.0556*(0.9).^n;
plot(v,'b--o')
legend('仿真数据','理论推导数据');grid on;
hold off

% 清空所有变量，关闭所有图形窗口，清空命令行窗口
clear all; close all; clc;

% 定义滤波器系数
b = [1, 0, -1]; % 分子系数
a = [1, 0, -0.81]; % 分母系数

% 定义输入信号
x = ones(1, 100); % 长度为 100 的单位阶跃信号

% 使用滤波器对输入信号进行滤波
y = filter(b, a, x); % 计算滤波器的输出信号 y

% 绘制滤波器输出信号
plot(y, 'k-x'); % 绘制滤波器输出信号，黑色实线带 x 标记
xlabel('n'); % 设置 x 轴标签
ylabel('y(n)'); % 设置 y 轴标签
hold on; % 保持当前图形

% 定义理论推导数据
n = 0:99; % 时间序列 n
v = -0.0556 * (-0.9).^n + 1.0556 * (0.9).^n; % 理论推导的输出信号

% 绘制理论推导数据
plot(v, 'b--o'); % 绘制理论推导数据，蓝色虚线带 o 标记

% 添加图例
legend('仿真数据', '理论推导数据'); % 添加图例

% 显示网格线
grid on; % 显示网格线

% 结束保持
hold off; % 结束保持当前图形
