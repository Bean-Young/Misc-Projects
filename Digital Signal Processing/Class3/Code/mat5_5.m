clear; clc; close all; % 清空所有变量，清空命令行窗口，关闭所有图形窗口

% 定义滤波器系数
b = [2, -3.4, 5.5]; % 分子系数
a = [1, -1.2, 0.8]; % 分母系数

% 生成随机噪声信号
x = randn(1, 100); % 生成长度为 100 的随机噪声信号

% 使用滤波器对随机噪声信号进行滤波
y = filter(b, a, x); % 计算滤波器的输出信号 y

% 绘制滤波器输出信号
plot(y); % 绘制滤波器输出信号
title('随机噪声响应曲线'); % 设置图形标题
xlabel('t (s)'); % 设置 x 轴标签
ylabel('幅度'); % 设置 y 轴标签
grid on; % 显示网格线
